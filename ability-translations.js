/**
 * Traduit le slug d'un talent de l'anglais vers le français.
 * Si le talent n'est pas dans le dictionnaire, retourne le slug capitalisé par défaut.
 * @param {string} slugEn - Le nom du talent provenant de PokéAPI (ex: "intimidate")
 * @returns {string} Le nom en français (ex: "Intimidation")
 */
function translateAbility(slugEn) {
  if (!slugEn) return "";
  
  // Normalisation de la clé (minuscules, suppression des espaces)
  const key = slugEn.toLowerCase().trim().replace(/\s+/g, '-');
  
  if (ABILITY_TRANSLATIONS[key]) {
    return ABILITY_TRANSLATIONS[key];
  }
  
  // Fallback propre si le talent est absent
  return slugEn.charAt(0).toUpperCase() + slugEn.slice(1).replace(/-/g, ' ');
}